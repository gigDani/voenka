      <p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Русское географическое общество было основано по Высочайшему повелению императора Николая I в 1845 году.&nbsp;18 августа (6 августа по старому стилю) 1845 года император утвердил временный устав РГО.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Идея создания Общества принадлежала адмиралу Фёдору Петровичу Литке, воспитателю будущего первого Председателя Русского географического общества Великого князя Константина Николаевича. Главной задачей новой организации было собрать и направить лучшие молодые силы России на всестороннее изучение родной земли.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif"><a data-cbox-img-attrs="{&quot;title&quot;: &quot;&quot;, &quot;alt&quot;: &quot;&quot;}" data-colorbox-gallery="gallery-file-219997-ssbIYHb2dcQ" href="https://www.rgo.ru/sites/default/files/styles/full_view/public/media/2020-11-16/kollazh3.jpg?itok=oEA3MtyE" title=""><img alt="" data-delta="4" src="https://www.rgo.ru/sites/default/files/styles/article_full/public/media/2020-11-16/kollazh3.jpg?itok=IFeIHP8S" title="" typeof="foaf:Image" /></a></span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Среди учредителей Русского географического общества были знаменитые мореплаватели: адмиралы Фёдор Петрович Литке, Иван Фёдорович Крузенштерн, Фердинанд Петрович Врангель, Пётр Иванович Рикорд; члены Петербургской Академии наук: естествоиспытатель Карл Максимович Бэр, астроном Василий Яковлевич Струве, геолог Григорий Петрович&nbsp;Гельмерсен, статистик Пётр Иванович Кеппен; видные военные деятели (бывшие и действующие офицеры Генерального штаба): генерал-квартирмейстер Фёдор Фёдорович&nbsp;Берг, геодезист Михаил Павлович Вронченко, государственный деятель Михаил Николаевич Муравьев; представители русской интеллигенции: лингвист Владимир Иванович Даль и меценат князь Владимир Фёдорович Одоевский.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Вот как охарактеризовал сущность Русского географического общества знаменитый географ, путешественник и государственный деятель Пётр Петрович Семёнов-Тян-Шанский:&nbsp;<em>&quot;Свободная и открытая для всех, кто проникнут любовью к родной земле и глубокой, несокрушимой верой в будущность Русского государства и русского народа, корпорация&quot;</em>.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif"><a data-cbox-img-attrs="{&quot;title&quot;: &quot;&quot;, &quot;alt&quot;: &quot;&quot;}" data-colorbox-gallery="gallery-file-88109-ssbIYHb2dcQ" href="https://www.rgo.ru/sites/default/files/styles/full_view/public/media/2017-02-13/niva_1890_1.jpg?itok=FnAoFlfV" title=""><img alt="" data-delta="1" src="https://www.rgo.ru/sites/default/files/styles/article_full/public/media/2017-02-13/niva_1890_1.jpg?itok=gZBFj_-K" title="" typeof="foaf:Image" /></a></span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">С момента основания Русское географическое общество не прекращало своей деятельности, однако название организации неоднократно изменялось: свое современное имя оно носило в 1845&ndash;1850, 1917&ndash;1926 и с 1992 года по настоящее время. Именовалось Императорским с 1850 по 1917 год. В советское время назвалось Государственным географическим обществом (1926&ndash;1938) и Географическом обществом Союза ССР (или Всесоюзным географическим обществом) (1938&ndash;1992).</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">В разные годы Русским географическим обществом руководили представители&nbsp;Императорского дома Романовых, знаменитые путешественники, исследователи и государственные деятели. Председателями Русского географического общества были: Великие князья Константин Николаевич (1845&ndash;1892) и Николай Михайлович (1892&ndash;1917), а Вице-председателями являлись: Фёдор Петрович Литке (1845&ndash;1850, 1857&ndash;1872), Михаил Николаевич Муравьёв (1850&ndash;1856), Пётр Петрович Семёнов-Тян-Шанский (1873&ndash;1914), Юлий Михайлович Шокальский (1914&ndash;1917), с 1917 по 1931 год являющийся Председателем Общества. С 1931 года Обществом руководили Президенты: Николай Иванович Вавилов (1931&ndash;1940), Лев Семёнович Берг (1940&ndash;1950), Павловский Евгений Никанорович (1952&ndash;1964), Станислав Викентьевич Калесник (1964&ndash;1977), Алексей Фёдорович Трёшников (1977&ndash;1991), Сергей Борисович Лавров (1991&ndash;2000), Юрий Петрович Селиверстов (2000&ndash;2002), Анатолий Александрович Комарицын(2002&ndash;2009), Сергей Кужугетович Шойгу (2009 &ndash; по настоящее время).</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Русское географическое общество внесло крупнейший вклад в изучение Европейской России, Урала, Сибири, Дальнего Востока, Средней и Центральной Азии, Кавказа, Ирана, Индии, Новой Гвинеи, полярных стран и других территорий. Эти исследования связаны с именами известных путешественников, таких как Николай Алексеевич Северцов, Иван Васильевич Мушкетов, Николай Михайлович Пржевальский, Григорий Николаевич Потанин, Михаил Васильевич Певцов, Григорий Ефимович и Михаил Ефимович Грумм-Гржимайло, Пётр Петрович Семёнов-Тян-Шанский, Владимир Афанасьевич Обручев, Пётр Кузьмич Козлов, Николай Николаевич Миклухо-Маклай, Александр Иванович Воейков, Лев Семёнович Берг и многие другие.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif"><a data-cbox-img-attrs="{&quot;title&quot;: &quot;Карта, составленная Н.Н. Миклухо-Маклаем&quot;, &quot;alt&quot;: &quot;Карта, составленная Н.М. Миклухо-Маклаем&quot;}" data-colorbox-gallery="gallery-file-88114-ssbIYHb2dcQ" href="https://www.rgo.ru/sites/default/files/styles/full_view/public/media/2017-02-13/map_mikluho-maklai_1.jpg?itok=3D4yuVyu" title="Карта, составленная Н.Н. Миклухо-Маклаем"><img alt="Карта, составленная Н.М. Миклухо-Маклаем" data-delta="1" src="https://www.rgo.ru/sites/default/files/styles/article_full/public/media/2017-02-13/map_mikluho-maklai_1.jpg?itok=EdMou9h4" title="Карта, составленная Н.Н. Миклухо-Маклаем" typeof="foaf:Image" /></a></span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Карта, составленная Н.Н. Миклухо-Маклаем</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Также важной традицией Русского географического общества является связь с русским флотом и морскими экспедициями. В числе действительных членов Общества были знаменитые морские исследователи: Пётр Фёдорович Анжу, Василий Степанович Завойко, Загоскин Лаврентий Алексеевич, Платон Юрьевич Лисянский, Фёдор Фёдорович Матюшкин, Геннадий Иванович Невельской, Константин Николаевич Посьет, Степан Осипович Макаров.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">В императорский период почётными членами Общества избирались члены иностранных королевских фамилий (например, личный друг Петра Петровича Семёнова-Тян-Шанского бельгийский король Леопольд Второй, турецкий султан Абдул Гамид, британский принц Альберт), известные иностранные исследователи и географы (барон Фердинанд Рихтгофен, Роальд Амудсен, Фритьоф Нансен и другие).</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Крупнейшими благотворителями, направлявшими значительные средства на деятельность Общества, были: купец Платон Васильевич Голубков, табачный фабрикант Василий Григорьевич Жуков, именем которого была названа одна из престижнейших премий Императорского Русского географического общества &ndash; Жуковская. Особое место среди меценатов Русского географического общества занимают золотопромышленники Сибиряковы, финансировавшие целый ряд экспедиционных и просветительских проектов.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">В 1851 году открылись два первых региональных отдела Русского географического общества: Кавказский в Тифлисе и Сибирский в Иркутске. Затем создаются новые отделы: Оренбургский, Северо-Западный в Вильно, Юго-Западный в Киеве, Западно-Сибирский в Омске, Приамурский в Хабаровске, Туркестанский в Ташкенте. Они проводили обширные исследования своих регионов. К 1917 году Императорское Русское географическое общество насчитывало 11 отделов (включая штаб-квартиру в Санкт-Петербурге), два подотдела и четыре отделения.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif"><a data-cbox-img-attrs="{&quot;title&quot;: &quot;Открытка 1916-го года с изображением фасада здания Общества в Санкт-Петербурге &quot;, &quot;alt&quot;: &quot;Открытка 1916-го года с изображением фасада здания Общества в Санкт-Петербурге &quot;}" data-colorbox-gallery="gallery-file-88111-ssbIYHb2dcQ" href="https://www.rgo.ru/sites/default/files/styles/full_view/public/media/2017-02-13/otkrytka4-1.jpg?itok=SucZZuyJ" title="Открытка 1916-го года с изображением фасада здания Общества в Санкт-Петербурге "><img alt="Открытка 1916-го года с изображением фасада здания Общества в Санкт-Петербурге " data-delta="1" src="https://www.rgo.ru/sites/default/files/styles/article_full/public/media/2017-02-13/otkrytka4-1.jpg?itok=zCxprhD3" title="Открытка 1916-го года с изображением фасада здания Общества в Санкт-Петербурге " typeof="foaf:Image" /></a></span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Открытка 1916-го года с изображением фасада здания Общества в Санкт-Петербурге</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">В советское время работа Общества изменилась. Русское географическое общество сосредоточилось на относительно небольших, но глубоких и всесторонних региональных исследованиях, а также крупных теоретических обобщениях. Значительно расширилась география региональных отделений: по состоянию на 1989&ndash;1992 годы в Географическом Обществе СССР работало Центральное отделение (в Ленинграде) и 14 республиканских отделений. В РСФСР насчитывалось 18 филиалов, два бюро и 78 отделов.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Русским географическим обществом были заложены и основы отечественного заповедного дела, идеи первых российских особо&nbsp;охраняемых&nbsp;природных&nbsp;территории &nbsp;рождались в рамках Постоянной Природоохранительной комиссии Императорского Русского географического общества, создателем которой был академик Иван Парфеньевич Бородин.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Важнейшим событием стало создание Постоянной комиссии Императорского Русского географического общества по изучению Арктики. Итогом её работы стали всемирно известные Чукотская, Якутская и Кольская экспедиции. Отчёт об одной из арктических экспедиций общества заинтересовал великого учёного Дмитрия Ивановича Менделеева, разработавшего несколько проектов освоения и исследования Арктики.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">Русское географическое общество стало одним из организаторов и участников первого Международного полярного года, в ходе которого были созданы автономные полярные станции в устье Лены и на Новой Земле.</span></p>

<p><span style="font-family:Lucida Sans Unicode,Lucida Grande,sans-serif">При содействии Русского географического общества в 1918 году было создано первое в мире высшее учебное заведение географического профиля &ndash; Географический институт. А в 1919 году одним из наиболее известных членов Общества Вениамином Петровичем Семеновым-Тян-Шанским был основан первый в России географический музей, в период расцвета его коллекции занимали третье место в России после Эрмитажа и Русского музея.</span></p>

